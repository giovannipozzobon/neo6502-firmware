		.section code	

EXPUnaryTime: ;; [time(]
		jsr 	ERRCheckRParen 					; )

		.DoSendMessage 							; get time.
		.byte 	1,1
		.DoWaitMessage
		
EXPReturn32BitInteger:		
		lda 	ControlParameters+0				; return as integer
		sta 	XSNumber0,x
		lda 	ControlParameters+1
		sta 	XSNumber1,x
		lda 	ControlParameters+2
		sta 	XSNumber2,x
		lda 	ControlParameters+3
		sta 	XSNumber3,x
		stz 	XSControl,x
		rts

		.send code

				
